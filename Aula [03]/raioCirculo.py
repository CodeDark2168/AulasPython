#Faça um codigo que leia do usuário o raio do círculo e calcule a área
print("==============================")
raio = float(input("Digite o valor do raio do círculo em centímetro: "))
area = 3.1415*(raio*raio)
aream= area/100
print("A área do círculo é igual a " + str(area) + " centímetros ou " + str(aream) + " metros.")
print("==============================")